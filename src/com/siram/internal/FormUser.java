    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String kode = txtid.getText();
        String nama = txtnamauser.getText();
        String username = txtusername.getText();
        String password = txtpassword.getText();
        String akses = (String) cbakses.getSelectedItem();
        Connection con = com.siram.koneksi.Koneksi.getKoneksi();
        try {
            if(kode.equals("")||nama.equals("")||username.equals("")||password.equals("")||akses.equals("")) {
                JOptionPane.showMessageDialog(this, "Data ada yang kosong", "pesan", JOptionPane.ERROR_MESSAGE);
            } else {
                Statement st = con.createStatement();
                String sql = "INSERT INTO users(id,nama,username,password,hak_akses) VALUES('"+kode+"','"+nama+"',"
                        + "'"+username+"','"+password+"','"+akses+"')";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(rootPane, "Data berhasil di simpan");
            }
        } catch(SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Data gagal tersimpan", "Pesan", JOptionPane.WARNING_MESSAGE);
        }
        kosongkan();
        getData();
    }//GEN-LAST:event_btnsimpanActionPerformed

