    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        try {
            int row =tabel.getSelectedRow();
            String tabel_klik=(tabel.getModel().getValueAt(row, 0).toString());
            Connection con =(Connection)com.siram.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet sql = st.executeQuery("SELECT * FROM users WHERE id='"+tabel_klik+"'");
            if(sql.next()){
                String kode = sql.getString("id");
                txtid.setText(kode);
                String nama = sql.getString("nama");
                txtnamauser.setText(nama);
                String username = sql.getString("username");
                txtusername.setText(username);
                String password = sql.getString("password");
                txtpassword.setText(password);
                String akses = sql.getString("hak_akses");
                cbakses.setSelectedItem(akses);
            } 
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tabelMouseClicked

    public void kosongkan() {
        txtid.setText("");
        txtnamauser.setText("");
        txtusername.setText("");
        txtpassword.setText("");
        cbakses.setSelectedIndex(1);
    }
    
