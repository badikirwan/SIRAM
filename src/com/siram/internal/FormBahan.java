    private void getData() {
        DefaultTableModel tbm = new DefaultTableModel();
        tbm.addColumn("KODE");
        tbm.addColumn("NAMA BAHAN");
        tbm.addColumn("SATUAN");
        tbm.addColumn("HARGA");
        tbm.addColumn("STOK");
        try {
            Connection con = (Connection)com.siram.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from bahan");
            while(rs.next()) {
                tbm.addRow(new Object[] {
                    rs.getString("kode_bahan"), rs.getString("nama_bahan"), rs.getString("satuan"), rs.getString("harga"),
                    rs.getString("stok")});
               tabel.setModel(tbm);
            }
        } catch(Exception e) {
            
        }
       //AutoSizeTable();
    }

