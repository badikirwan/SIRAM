    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        try {
            int row =tabel.getSelectedRow();
            String tabel_klik=(tabel.getModel().getValueAt(row, 0).toString());
            Connection con =(Connection)com.siram.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet sql = st.executeQuery("SELECT * FROM bahan WHERE kode_bahan='"+tabel_klik+"'");
            if(sql.next()){
                String kode = sql.getString("kode_bahan");
                txtkodebahan.setText(kode);
                String nama = sql.getString("nama_bahan");
                txtnamabahan.setText(nama);
                String satuan = sql.getString("satuan");
                txtsatuan.setText(satuan);
                String harga = sql.getString("harga");
                txtharga.setText(harga);
                String stok = sql.getString("stok");
                txtstok.setText(stok);
            } 
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tabelMouseClicked

    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        // TODO add your handling code here:
        String kode = txtkodebahan.getText();
        String nama = txtnamabahan.getText();
        String satuan = txtsatuan.getText();
        String harga = txtharga.getText();
        String stok = txtstok.getText();
        Connection con = com.siram.koneksi.Koneksi.getKoneksi();
        try {
            if(kode.equals("")||nama.equals("")||harga.equals("")||stok.equals("")) {
                JOptionPane.showMessageDialog(this, "Data ada yang kosong", "pesan", JOptionPane.ERROR_MESSAGE);
            } else {
                Statement st = con.createStatement();
                String sql = "INSERT INTO bahan(kode_bahan,nama_bahan,satuan,harga,stok) VALUES('"+kode+"','"+nama+"',"
                        + "'"+satuan+"','"+harga+"','"+stok+"')";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(rootPane, "Data berhasil di simpan");
            }
        } catch(SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Data gagal tersimpan", "Pesan", JOptionPane.WARNING_MESSAGE);
        }
        kosongkan();
        getData();
    }//GEN-LAST:event_btnsimpanActionPerformed

