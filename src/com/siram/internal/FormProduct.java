    public void bersihkan() {
        txtkode.setText("");
        txtnamaproduk.setText("");
        txtsatuan.setText("");
        txtharga.setText("");
        cbjenis.setSelectedItem(ABORT);
    }
    
    public void getData() {
        DefaultTableModel tbm = new DefaultTableModel();
        tbm.addColumn("KODE");
        tbm.addColumn("NAMA PRODUK");
        tbm.addColumn("SATUAN");
        tbm.addColumn("HARGA");
        tbm.addColumn("JENIS");
        try {
            Connection con = (Connection)com.siram.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from produk");
            while(rs.next()) {
                tbm.addRow(new Object[] {
                    rs.getString("kode"), rs.getString("nama"), rs.getString("satuan"), rs.getString("harga"),
                    rs.getString("jenis")});
               tabel.setModel(tbm);
            }
        } catch(Exception e) {
            
        }
       //AutoSizeTable();
    }

