    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
        // TODO add your handling code here:
        String kode = txtkode.getText();
        String nama = txtnamaproduk.getText();
        String satuan = txtsatuan.getText();
        String harga = txtharga.getText();
        String jenis = (String) cbjenis.getSelectedItem();
        Connection con = (Connection)com.siram.koneksi.Koneksi.getKoneksi();
         try {
            if(kode.equals("")||nama.equals("")||harga.equals("")||satuan.equals("")||jenis.equals("")) {
                JOptionPane.showMessageDialog(this, "Data ada yang kosong", "pesan", JOptionPane.ERROR_MESSAGE);
            } else {
                Statement st = con.createStatement();
                 String sql = "UPDATE produk SET kode='"+kode+"',nama='"+nama+"',satuan='"+satuan+"',"
                       + "harga='"+harga+"',jenis='"+jenis+"' WHERE kode='"+kode+"'"; 
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(rootPane, "Data berhasil di diedit");
            } 
            
        } catch(SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Data gagal diedit", "Pesan", JOptionPane.WARNING_MESSAGE);
        }
        bersihkan();
        getData();

    }//GEN-LAST:event_btneditActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        // TODO add your handling code here:
        String kode = txtkode.getText();
        String nama = txtnamaproduk.getText();
        String satuan = txtsatuan.getText();
        String harga = txtharga.getText();
        String jenis = (String) cbjenis.getSelectedItem();
        Connection con = (Connection)com.siram.koneksi.Koneksi.getKoneksi();
        int baris = tabel.getSelectedRow();
        try {
            Statement st = con.createStatement();
            if(baris == -1) {
                JOptionPane.showMessageDialog(rootPane, "Anda belum memilih tabel");
            } else {
                String sql = "DELETE FROM produk WHERE kode='"+tabel.getModel().getValueAt(baris, 0)+"'";
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Data berhasil di hapus");
            }
            
        } catch(Exception e) {
            
        }
        bersihkan();
        getData();
    }//GEN-LAST:event_btnhapusActionPerformed

