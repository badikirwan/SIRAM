    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
         try {
            int row =tabel.getSelectedRow();
            String tabel_klik=(tabel.getModel().getValueAt(row, 0).toString());
            Connection con =(Connection)com.siram.koneksi.Koneksi.getKoneksi();
            Statement st = con.createStatement();
            ResultSet sql = st.executeQuery("SELECT * FROM produk WHERE kode='"+tabel_klik+"'");
            if(sql.next()){
                String kode = sql.getString("kode");
                txtkode.setText(kode);
                String nama = sql.getString("nama");
                txtnamaproduk.setText(nama);
                String satuan = sql.getString("satuan");
                txtsatuan.setText(satuan);
                String harga = sql.getString("harga");
                txtharga.setText(harga);
                String jenis = sql.getString("jenis");
                cbjenis.setSelectedItem(jenis);
            } 
        } catch (Exception e) {
        }
    }//GEN-LAST:event_tabelMouseClicked

